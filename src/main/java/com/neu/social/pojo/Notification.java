package com.neu.social.pojo;

public class Notification {

	private String notificationId;
	private String notificationTitle;
	private String notificationDescription;
	private String notifcationImage;
	public String getNotificationId() {
		return notificationId;
	}
	public void setNotificationId(String notificationId) {
		this.notificationId = notificationId;
	}
	public String getNotificationTitle() {
		return notificationTitle;
	}
	public void setNotificationTitle(String notificationTitle) {
		this.notificationTitle = notificationTitle;
	}
	public String getNotificationDescription() {
		return notificationDescription;
	}
	public void setNotificationDescription(String notificationDescription) {
		this.notificationDescription = notificationDescription;
	}
	public String getNotifcationImage() {
		return notifcationImage;
	}
	public void setNotifcationImage(String notifcationImage) {
		this.notifcationImage = notifcationImage;
	}
	
	
}
